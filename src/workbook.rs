/// Encode one BrtBundleSh record payload.
fn encode_bundle_sh(tab_id: u32, rel_id: &str, name: &str) -> Vec<u8> {
    let mut pay = Vec::new();
    pay.extend_from_slice(&0u32.to_le_bytes()); // hsState = visible
    pay.extend_from_slice(&tab_id.to_le_bytes());
    write_wstr(rel_id, &mut pay);
    write_wstr(name, &mut pay);
    pay
}

/// Build a complete workbook.bin for the given sheet names.
pub fn build_workbook(names: &[&str]) -> Vec<u8> {
    let mut out = Vec::with_capacity(WB_PREFIX.len() + names.len() * 50 + WB_SUFFIX.len());
    out.extend_from_slice(WB_PREFIX);
    for (i, name) in names.iter().enumerate() {
        let tab_id = (i + 1) as u32;
        let rel_id = format!("rId{}", tab_id);
        let pay = encode_bundle_sh(tab_id, &rel_id, name);
        write_rec(0x009C, &pay, &mut out);
    }
    out.extend_from_slice(WB_SUFFIX);
    out
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_single_sheet1_length() {
        // Single Sheet1 should produce 531 bytes (matches the reference workbook.bin)
        let wb = build_workbook(&["Sheet1"]);
        assert_eq!(wb.len(), 531, "Sheet1 workbook should be 531 bytes");
    }

    #[test]
    fn test_multi_sheet_grows() {
        let wb2 = build_workbook(&["Sales", "Finance"]);
        let wb3 = build_workbook(&["A", "B", "C"]);
        assert!(wb2.len() > 531);
        assert!(wb3.len() > wb2.len());
    }
}
