// ── Format registry ───────────────────────────────────────────────────────────

pub fn resolve_shorthand(s: &str) -> &str {
    match s.to_lowercase().as_str() {
        "general"    => "General",
        "int"        => "#,##0",
        "int0"       => "0",
        "float1"     => "#,##0.0",
        "float2"     => "#,##0.00",
        "float3"     => "#,##0.000",
        "float4"     => "#,##0.0000",
        "pct"        => "0%",
        "pct1"       => "0.0%",
        "pct2"       => "0.00%",
        "sci"        => "0.00E+00",
        "date"       => "YYYY-MM-DD",
        "datetime"   => "YYYY-MM-DD HH:MM:SS",
        "time"       => "HH:MM:SS",
        "text"       => "@",
        "accounting" => "_($* #,##0.00_);_($* (#,##0.00);_($* \"-\"??_);_(@_)",
        "currency"   => "$#,##0.00",
        "euro"       => "€#,##0.00",
        _            => s,
    }
}

pub fn builtin_ifmt(fmt: &str) -> Option<u16> {
    match fmt {
        "General"     => Some(0),
        "0"           => Some(1),
        "0.00"        => Some(2),
        "#,##0"       => Some(3),
        "#,##0.00"    => Some(4),
        "0%"          => Some(9),
        "0.0%"        => Some(10),
        "0.00%"       => Some(10),
        "0.00E+00"    => Some(11),
        "m/d/yyyy"    => Some(14),
        "@"           => Some(49),
        _             => None,
    }
}

pub struct StylesBuilder {
    pub custom_fmts: IndexMap<String, u16>,  // fmt_string -> ifmt (164+)
    pub xf_map: IndexMap<u16, u16>,          // ifmt -> xf_index
}

impl StylesBuilder {
    pub fn new() -> Self {
        let mut xf_map = IndexMap::new();
        xf_map.insert(0, 0);   // XF[0] = General
        xf_map.insert(14, 1);  // XF[1] = Date m/d/yyyy
        Self { custom_fmts: IndexMap::new(), xf_map }
    }

    pub fn resolve_fmt(&mut self, fmt: &str) -> u16 {
        let fmt = resolve_shorthand(fmt);
        if let Some(id) = builtin_ifmt(fmt) { return id; }
        let next_id = 164 + self.custom_fmts.len() as u16;
        *self.custom_fmts.entry(fmt.to_owned()).or_insert(next_id)
    }

    pub fn xf_for_ifmt(&mut self, ifmt: u16) -> u16 {
        let next = self.xf_map.len() as u16;
        *self.xf_map.entry(ifmt).or_insert(next)
    }

    /// Index of the bold XF — always appended last by build().
    /// Call this AFTER all xf_for_ifmt() calls.
    pub fn bold_xf_index(&self) -> u16 {
        self.xf_map.len() as u16
    }

    /// Build styles.bin — mirrors Python _build_styles() exactly.
    pub fn build(&self, bold_header: bool) -> Vec<u8> {
        let custom_fmts = &self.custom_fmts;
        // extra_xfs: all XFs except General(0) and Date(14)
        let extra_xfs: Vec<u16> = {
            let mut v: Vec<(u16, u16)> = self.xf_map.iter()
                .filter(|(&ifmt, _)| ifmt != 0 && ifmt != 14)
                .map(|(&ifmt, &idx)| (ifmt, idx))
                .collect();
            v.sort_by_key(|&(_, idx)| idx);
            v.into_iter().map(|(ifmt, _)| ifmt).collect()
        };

        if custom_fmts.is_empty() && extra_xfs.is_empty() && !bold_header {
            return STYLES_BASE.to_vec();
        }

        // Build BrtFmt records for custom formats
        let mut fmt_recs: Vec<u8> = Vec::new();
        for (fmt_str, &ifmt) in custom_fmts {
            let mut pay = Vec::new();
            pay.extend_from_slice(&ifmt.to_le_bytes());
            write_wstr(fmt_str, &mut pay);
            write_rec(0x002C, &pay, &mut fmt_recs);
        }

        let mut base: Vec<u8> = STYLES_BASE.to_vec();

        // If bold_header: splice in bold font before BrtEndFonts
        // Python: find 'e30404' (BrtBeginFonts tag+len), patch count, insert before 'e404'
        if bold_header {
            let fnt_tag = &[0xe3u8, 0x04, 0x04];
            let fnt_pos = find_bytes(&base, fnt_tag).expect("BrtBeginFonts not found");
            let fnt_count_pos = fnt_pos + 3; // skip tag(2 bytes varint) + len(1 byte)
            let old_count = u32::from_le_bytes(base[fnt_count_pos..fnt_count_pos+4].try_into().unwrap());
            let efnt_pos = find_bytes_from(&base, &[0xe4u8, 0x04], fnt_pos).expect("BrtEndFonts not found");
            // Build bold font record
            let mut bold_rec: Vec<u8> = Vec::new();
            write_rec(0x002B, BOLD_FONT, &mut bold_rec);
            // Splice: patch count + insert bold font before BrtEndFonts
            let mut new_base: Vec<u8> = Vec::with_capacity(base.len() + bold_rec.len());
            new_base.extend_from_slice(&base[..fnt_count_pos]);
            new_base.extend_from_slice(&(old_count + 1).to_le_bytes());
            new_base.extend_from_slice(&base[fnt_count_pos+4..efnt_pos]);
            new_base.extend_from_slice(&bold_rec);
            new_base.extend_from_slice(&base[efnt_pos..]);
            base = new_base;
        }

        // Find BrtBeginCellXFs and BrtEndCellXFs using dynamic search
        // Python: find 'e90404' tag, get count, find 'ea04' end
        let bci_tag = &[0xe9u8, 0x04, 0x04];
        let bci_pos = find_bytes(&base, bci_tag).expect("BrtBeginCellXFs not found");
        let count_pos = bci_pos + 3;
        let old_count = u32::from_le_bytes(base[count_pos..count_pos+4].try_into().unwrap());
        let eci_pos = find_bytes_from(&base, &[0xeau8, 0x04], bci_pos).expect("BrtEndCellXFs not found");

        let extra_count = extra_xfs.len() as u32 + if bold_header { 1 } else { 0 };
        let new_count = old_count + extra_count;

        // Build extra XF records
        let mut xf_recs: Vec<u8> = Vec::new();
        for ifmt in &extra_xfs {
            let mut xf = [0u8; 16];
            xf[2..4].copy_from_slice(&ifmt.to_le_bytes());
            xf[10..14].copy_from_slice(&0x10100000u32.to_le_bytes());
            write_rec(0x002F, &xf, &mut xf_recs);
        }
        if bold_header {
            // Bold XF: ifnt=1, grbitAtr=0x0002 (fAtrFnt)
            let mut xf = [0u8; 16];
            xf[4..6].copy_from_slice(&1u16.to_le_bytes());  // ifnt=1
            xf[10..14].copy_from_slice(&0x10100000u32.to_le_bytes());
            xf[14..16].copy_from_slice(&0x0002u16.to_le_bytes()); // grbitAtr fAtrFnt
            write_rec(0x002F, &xf, &mut xf_recs);
        }

        // Assemble: base[0:3] + fmt_recs + base[3..count_pos] + new_count + base[count_pos+4..eci_pos] + xf_recs + base[eci_pos..]
        let mut out: Vec<u8> = Vec::with_capacity(base.len() + fmt_recs.len() + xf_recs.len());
        out.extend_from_slice(&base[..3]);
        out.extend_from_slice(&fmt_recs);
        out.extend_from_slice(&base[3..count_pos]);
        out.extend_from_slice(&new_count.to_le_bytes());
        out.extend_from_slice(&base[count_pos+4..eci_pos]);
        out.extend_from_slice(&xf_recs);
        out.extend_from_slice(&base[eci_pos..]);
        out
    }
}

impl Default for StylesBuilder {
    fn default() -> Self { Self::new() }
}

fn find_bytes(haystack: &[u8], needle: &[u8]) -> Option<usize> {
    haystack.windows(needle.len()).position(|w| w == needle)
}

fn find_bytes_from(haystack: &[u8], needle: &[u8], from: usize) -> Option<usize> {
    haystack[from..].windows(needle.len()).position(|w| w == needle).map(|i| i + from)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_styles_base_verbatim() {
        let sb = StylesBuilder::new();
        assert_eq!(sb.build(false), STYLES_BASE, "No-custom styles must match reference exactly");
    }

    #[test]
    fn test_bold_adds_font_and_xf() {
        let sb = StylesBuilder::new();
        let out = sb.build(true);
        assert!(out.len() > STYLES_BASE.len());
        // Bold font bls=700 bytes (0xbc02 LE)
        assert!(out.windows(2).any(|w| w == [0xbc, 0x02]));
    }

    #[test]
    fn test_custom_fmt() {
        let mut sb = StylesBuilder::new();
        let ifmt = sb.resolve_fmt("float2");
        assert_eq!(ifmt, 4); // "#,##0.00" is builtin 4
        let xf = sb.xf_for_ifmt(ifmt);
        assert!(xf >= 2);
    }
}
